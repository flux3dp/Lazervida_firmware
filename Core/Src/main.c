/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();
  MX_GPIO_Init();
  /* USER CODE BEGIN SysInit */
  // =============== FLUX dedicated code ===============
  MX_USART1_UART_Init();
  debugString("Beam Air Pro: 0.0.4\n");

  
  HAL_Delay(200);

  
  // ===============================1====================

  /* USER CODE END SysInit */

  #if DEBUG_SERIAL_ON
  debugString("Clock Enabled\n");
  #endif
  
  /* Initialize all configured peripherals */
  MX_DMA_Init();
  // MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2); // RLED
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1); // BLED
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2); // GLED
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3); // FAN
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */
  get_rcc_clock_config();
  #if DEBUG_SERIAL_ON
  debugString("RCC Clock inited\n");
  #endif

  // Initialize system upon power-up.
  serial_init();   // Setup serial baud rate and interrupts
  eeprom_init();
  settings_init(); // Load Grbl settings from EEPROM
  // system_init();   // Configure pinout pins and pin-change interrupt
  // MX_I2C1_Init();
  sensors_init();  // Initialize FLUX's custom sensors
  
  debugString("Sensor Inited\n");
  MX_USART3_UART_Init();
  debugString("Base Enabled!\n");

  // Enable USART1/USART3 IRQn
  HAL_NVIC_SetPriority(USART1_IRQn, 0, 0);
  HAL_NVIC_EnableIRQ(USART1_IRQn);
  HAL_NVIC_SetPriority(USART3_IRQn, 0, 1);
  HAL_NVIC_EnableIRQ(USART3_IRQn);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  memset(sys_probe_position,0,sizeof(sys_probe_position)); // Clear probe position.
  memset(sys_position,0,sizeof(sys_position)); // Clear machine position.
  sys.state = STATE_IDLE;

  HAL_UART_Receive_IT(&huart1, (uint8_t *)uart1_buffer, 1);
  HAL_UART_Receive_IT(&huart3, (uint8_t *)uart3_buffer, 9);

  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    // Reset system variables.
    uint8_t prior_state = sys.state;
    memset(&sys, 0, sizeof(system_t)); // Clear system struct variable.
    sys.state = prior_state;
    sys.f_override = DEFAULT_FEED_OVERRIDE;  // Set to 100%
    sys.r_override = DEFAULT_RAPID_OVERRIDE; // Set to 100%
    sys.spindle_speed_ovr = DEFAULT_SPINDLE_SPEED_OVERRIDE; // Set to 100%
    sys_probe_state = 0;
    sys_rt_exec_state = 0;
    sys_rt_exec_alarm = 0;
    sys_rt_exec_motion_override = 0;
    sys_rt_exec_accessory_override = 0;

    // Reset Grbl primary systems.
    serial_reset_read_buffer(); // Clear serial read buffer
    gc_init(); // Set g-code parser to default state

    // Sync cleared gcode and planner positions to current system position.

    // Print welcome message. Indicates an initialization has occured at power-up or with a reset.
    report_init_message();

    // Start Grbl main loop. Processes program inputs and executes them.
    protocol_main_loop();
  }
  /* USER CODE END 3 */
}

